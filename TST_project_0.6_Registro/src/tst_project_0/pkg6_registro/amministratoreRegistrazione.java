        String nome = textNome.getText();
        String password = String.valueOf(textPassword.getPassword());
        String passwordC = String.valueOf(textPasswordC.getPassword());
        String dataNascita = textData.getText();
        String comune = textComune.getText();
        String nazione = textNazione.getText(); 
        String informazioni = textAltro.getText();
        boolean text = false;
        boolean  prv = false;
        boolean sex = false;
        boolean DataPCom = false;
    if(password.equals(passwordC) && !(password.equals(""))&& !(nome.equals("")))
    {
        text = true;
    }else{
        text = false;
        JOptionPane.showMessageDialog(null,"Il nome utente o la Password non √® accetta, verificare i campi");      
    }
    if(radioAlunno.isSelected() || radioProfessore.isSelected())
        {
        prv = true;
        }else{
        prv = false;
        JOptionPane.showMessageDialog(null,"Selezionare i privilegi da assegnare all'utente");      
    }
    if(radioM.isSelected() ||radioF.isSelected()){
    sex= true;
    }else{
   sex= false;
  JOptionPane.showMessageDialog(null,"Selezionare il sesso");      
    }
if(!(nazione.equals("")) && !(dataNascita.equals(""))){
 DataPCom=true;
}else{
DataPCom=false;
JOptionPane.showMessageDialog(null,"campi data di nascita o nazione non inseriti o incorretti");      
}
if(text == true && prv ==true && sex==true && DataPCom==true){
    new registrazioneSystemInfo();
    new registrazioneSystemAccount();
    new registrazionePoteri();
}
